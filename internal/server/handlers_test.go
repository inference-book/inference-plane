// mockBackend is a Backend implementation whose methods are pluggable
// per-test. Lets each test case shape the upstream behavior precisely
// without spinning up an httptest server.
type mockBackend struct {
	name     string
	generate func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error)
	health   func(ctx context.Context) error
}

func (m *mockBackend) Name() string { return m.name }
func (m *mockBackend) Generate(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
	return m.generate(ctx, req)
}
func (m *mockBackend) Health(ctx context.Context) error {
	if m.health == nil {
		return nil
	}
	return m.health(ctx)
}

// silentLogger discards log output; we don't want test output cluttered
// with structured logs from the handler's error paths.
var silentLogger = slog.New(slog.NewTextHandler(io.Discard, nil))

// validReq is a minimal valid completion request used as the default
// payload; tests override fields as needed.
func validReq() backend.GenerateRequest {
	return backend.GenerateRequest{Model: "qwen", Prompt: "hello", MaxTokens: 4}
}

func okResp() backend.GenerateResponse {
	return backend.GenerateResponse{
		ID: "x", Object: "text_completion", Model: "qwen",
		Choices: []backend.Choice{{Index: 0, Text: "world", FinishReason: "stop"}},
		Usage:   backend.Usage{PromptTokens: 1, CompletionTokens: 1, TotalTokens: 2},
	}
}

// callHandler sends a synthetic request to a handler and returns the
// recorded response. Body is JSON-encoded from the supplied any (or
// passed through if it's a string -- used for malformed-JSON cases).
func callHandler(t *testing.T, h http.Handler, method, path string, body any) *httptest.ResponseRecorder {
	t.Helper()
	var bodyReader io.Reader
	switch v := body.(type) {
	case nil:
	case string:
		bodyReader = strings.NewReader(v)
	default:
		raw, err := json.Marshal(v)
		if err != nil {
			t.Fatalf("marshal request body: %v", err)
		}
		bodyReader = strings.NewReader(string(raw))
	}
	req := httptest.NewRequest(method, path, bodyReader)
	w := httptest.NewRecorder()
	h.ServeHTTP(w, req)
	return w
}

// ── Happy path ─────────────────────────────────────────────────────

func TestHandler_PlainCompletion_OK(t *testing.T) {
	be := &mockBackend{
		name: "test",
		generate: func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
			if req.Prompt != "hello" {
				t.Errorf("backend got wrong prompt: %q", req.Prompt)
			}
			return okResp(), nil
		},
	}
	h := &completionsHandler{backend: be, logger: silentLogger}
	w := callHandler(t, h, http.MethodPost, "/v1/completions", validReq())

	if w.Code != http.StatusOK {
		t.Fatalf("status = %d, want 200", w.Code)
	}
	var out backend.GenerateResponse
	if err := json.Unmarshal(w.Body.Bytes(), &out); err != nil {
		t.Fatalf("decode response: %v", err)
	}
	if out.Choices[0].Text != "world" {
		t.Errorf("response text = %q, want 'world'", out.Choices[0].Text)
	}
}

