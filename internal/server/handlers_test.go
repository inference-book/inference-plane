// ── Backend error mapping (Chapter 6.5 rules) ──────────────────────

// 4xx upstream passes through to the client unchanged. The body the
// upstream sent (e.g. a token-limit message) is forwarded so the client
// gets actionable detail.
func TestHandler_Backend4xx_PassesThrough(t *testing.T) {
	be := &mockBackend{
		generate: func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
			return backend.GenerateResponse{}, &backend.BackendError{
				Backend: "vllm", StatusCode: 422, Body: "context length exceeded",
			}
		},
	}
	h := &completionsHandler{backend: be, logger: silentLogger}
	w := callHandler(t, h, http.MethodPost, "/v1/completions", validReq())

	if w.Code != 422 {
		t.Errorf("status = %d, want 422", w.Code)
	}
	if !strings.Contains(w.Body.String(), "context length exceeded") {
		t.Errorf("expected upstream body to pass through, got: %s", w.Body.String())
	}
}

// 5xx upstream becomes 502 to the client. The original body is logged
// (we asserted that elsewhere via writeBackendError) but not surfaced
// to the client -- it's an internal detail.
func TestHandler_Backend5xx_BecomesBadGateway(t *testing.T) {
	be := &mockBackend{
		generate: func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
			return backend.GenerateResponse{}, &backend.BackendError{
				Backend: "vllm", StatusCode: 503, Body: "overloaded",
			}
		},
	}
	h := &completionsHandler{backend: be, logger: silentLogger}
	w := callHandler(t, h, http.MethodPost, "/v1/completions", validReq())

	if w.Code != http.StatusBadGateway {
		t.Errorf("status = %d, want 502", w.Code)
	}
}

// Context cancellation surfaces as 499 (client closed request). Lets
// metrics distinguish caller-cancelled work from real backend failures.
func TestHandler_ContextCancelled_BecomesClientClosed(t *testing.T) {
	be := &mockBackend{
		generate: func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
			return backend.GenerateResponse{}, context.Canceled
		},
	}
	h := &completionsHandler{backend: be, logger: silentLogger}
	w := callHandler(t, h, http.MethodPost, "/v1/completions", validReq())

	if w.Code != 499 {
		t.Errorf("status = %d, want 499", w.Code)
	}
}

// Anything else (network failure, decode failure, plain error) becomes
// a 502: we don't know enough to be more specific, but we know it's an
// upstream-side problem.
func TestHandler_GenericError_BecomesBadGateway(t *testing.T) {
	be := &mockBackend{
		generate: func(ctx context.Context, req backend.GenerateRequest) (backend.GenerateResponse, error) {
			return backend.GenerateResponse{}, errors.New("network unreachable")
		},
	}
	h := &completionsHandler{backend: be, logger: silentLogger}
	w := callHandler(t, h, http.MethodPost, "/v1/completions", validReq())

	if w.Code != http.StatusBadGateway {
		t.Errorf("status = %d, want 502", w.Code)
	}
}
